package be.vinci.ipl.chattycar.passengers.models;

public class User {
  private int id;
  private String email;
  private String firstname;
  private String lastname;

  public int getId() {
    return id;
  }

  public String getEmail() {
    return email;
  }

  public String getFirstname() {
    return firstname;
  }

  public String getLastname() {
    return lastname;
  }
}
